void chip8::init()
{

	pc = 0x200;		// Program counter starts at 0x200 (Start adress program)
	opcode = 0;			// Reset current opcode	
	I = 0;			// Reset index register
	sp = 0;			// Reset stack pointer

	// Clear display
	for (int i = 0; i < 2048; ++i)
		gfx[i] = 0;

	// Clear stack
	for (int i = 0; i < 16; ++i)
		stack[i] = 0;

	for (int i = 0; i < 16; ++i)
		key[i] = V[i] = 0;

	// Clear memory
	for (int i = 0; i < 4096; ++i)
		memory[i] = 0;

	// Load fontset
	for (int i = 0; i < 80; ++i)
		memory[i] = chip8_fontset[i];

	// Reset timers
	delay_timer = 0;
	sound_timer = 0;

	// Clear screen once
	drawFlag = true;

	srand(time(NULL));


}


bool chip8::loadApplication(const char * filename)
{
	init(); //http://www.cplusplus.com/doc/tutorial/files/

	ifstream readFileGame;

	readFileGame.open(filename, ifstream::in);

	if (!readFileGame.good())
	{
		cout << "File error" << endl;
		return false;
	}

	readFileGame.seekg(0, ios_base::end);
	long fSize = readFileGame.tellg();
	readFileGame.seekg(0);

	cout << "File size " << fSize << endl;



	readFileGame.close();




	cout << "Loading: %s\n" << filename << endl;

	// Open file
	FILE * pFile = fopen(filename, "rb");
	if (pFile == NULL)
	{
		fputs("File error", stderr);
		return false;
	}

	// Check file size
	fseek(pFile, 0, SEEK_END);
	long lSize = ftell(pFile);
	rewind(pFile);
	printf("Filesize: %d\n", (int)lSize);

	// Allocate memory to contain the whole file
	char * buffer = (char*)malloc(sizeof(char) * lSize);
	if (buffer == NULL)
	{
		fputs("Memory error", stderr);
		return false;
	}

	// Copy the file into the buffer
	size_t result = fread(buffer, 1, lSize, pFile);
	if (result != lSize)
	{
		fputs("Reading error", stderr);
		return false;
	}

	// Copy buffer to Chip8 memory
	if ((4096 - 512) > lSize)
	{
		for (int i = 0; i < lSize; ++i)
			memory[i + 512] = buffer[i];
	}
	else
		printf("Error: ROM too big for memory");

	// Close file, free buffer
	fclose(pFile);
	free(buffer);

	return true;
}
